        <h1>L'apprentissage des couleurs</h1>

        <p> L'apprentissage des couleurs représente un élément important que tout enfant devra maitriser pour son entrée
            à l'école.
        </p>

        <p>
            De façon générale, les enfants débuteront l'apprentissage des couleurs entre 2 et 3 ans.
            Les couleurs de base (rouge, vert, bleu, jaune) seront maitrisées d'abord.
            Les autres couleurs seront ensuite intégrées progressivement.
            Pour certains enfants, l'apprentissage des couleurs demeure difficile.
            Le concept des couleurs est un concept très abstrait; cela complexifie le tout. En gros, nous pouvons avoir
            les attentes suivantes :
        </p>

        <span>Entre 3 et 4 ans, les enfants peuvent regrouper des objets de la même couleur et nommer les couleurs de base;</span>
        <span>Vers 4 ans, ils réussissent à nommer les couleurs un peu plus difficiles;</span>
        <span>Vers 5 ans, ils comprennent les nuances de couleurs (couleurs pâles/foncées), même si cela demeure difficile pour eux.</span>

        <h2>Dans ce jeu :</h2>

        <p>
            Niveau 1 à 4,
            les enfants débutent l'apprentissage des couleurs en mode « compréhension ».
            Amenez d’abord les enfants à indiquer les couleurs qu'ils pointent avec la souris en bas de l'écran.
            Ensuite, ils pourront les glisser et déposer sur la couleur correspondante au centre de l'écran.
        <p>

        <p>
            Niveau 5 à 8,
            les enfants doivent apprendre à distinguer à la fois les formes et les couleurs.
        </p>

        <p>
            Niveau 9 à 10,
            les enfants apprenent à reconnaître et à organiser les couleurs des fruits et légumes.
            Ils devront pour cela comme pour les niveaux précédents sélectionner, glisser et déposer les fruits
            et légumes dans le panier correspondant à leur couleur.
        </p>


        <h2>Accompagnez votre enfant</h2>

        <p>
            Laisser vos enfants accéder aux ordinateurs et tablettes ne veut pas dire que les parents n’ont plus
            de rôle à jouer.
            Au contraire ! Comme un jouet traditionnel, vous êtes là pour expliquer la façon dont on s’en sert,
            le moment où l’on joue ou encore les limites à apprendre.
            Laissez votre enfant apprivoiser l'outil tout en étant là pour servir de cadre.
        </p>

        <h2>Un temps pour chaque âge</h2>

        <p>
            L'Association Française de Pédiatrie Ambulatoire recommande de suivre la règle 3-6-9-12.
            A chaque tranche d’âge son temps devant un écran : pas d’écran avant 3 ans, une heure par jour de 3
            à 6 ans, deux heures entre 6 et 9 ans et enfin trois heures pour les plus âgés.
            Cette préconisation concerne tous les écrans et pas seulement les tablettes.
        </p>

